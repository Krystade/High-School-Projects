public class AsteroidMan extends SuperHero{
    public AsteroidMan(){
        super();
    }
    public AsteroidMan(String suitColor, boolean hasCape){
        super();
        setSuitColor(suitColor);
        setCape(hasCape);
    }
    public String catchphrase(){
        return "Prepare to be exterminated!";
    }
}
