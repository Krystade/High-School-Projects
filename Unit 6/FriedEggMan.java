public class FriedEggMan extends SuperHero{
    public FriedEggMan(){
        super();
    }
    public FriedEggMan(String suitColor, boolean hasCape){
        super();
        setSuitColor(suitColor);
        setCape(hasCape);
    }
    public String catchphrase(){
        return "You're Toast!";
    }
}
